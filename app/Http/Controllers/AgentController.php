     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'id' => 'required',
            'userid' => 'required',
            'action' => 'required',
            'status' => 'required',
            'type' => 'required',
            'category' => 'required',
            'code' => 'required'
        ]);

        Agent::create($request->all());

        return ['message' => 'Agent data stored!'];
    }

